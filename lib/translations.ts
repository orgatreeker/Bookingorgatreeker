export const getDiagnosis = (
  answers: { questionId: number; answerId: number }[],
  lang: "en" | "hi"
): { diagnosis: string; recommendation: "starter" | "complete"; score: number } => {
  // Calculate score
  let score = 0;
  const incomeAnswer = answers.find((a) => a.questionId === 1)?.answerId || 1;
  const situationAnswer = answers.find((a) => a.questionId === 2)?.answerId || 1;
  const goalAnswer = answers.find((a) => a.questionId === 3)?.answerId || 1;
  const triedAnswer = answers.find((a) => a.questionId === 4)?.answerId || 1;
  const awarenessAnswer = answers.find((a) => a.questionId === 5)?.answerId || 1;

  // Income scoring
  if (incomeAnswer >= 4) score += 2;
  else if (incomeAnswer <= 2) score += 1;

  // Situation scoring
  if (situationAnswer >= 3) score += 2;

  // Goal scoring
  if (goalAnswer >= 4) score += 2;
  else if (goalAnswer === 3) score += 1;

  // Tried before scoring
  if (triedAnswer === 4) score += 2;
  else if (triedAnswer === 3) score += 1;

  // Awareness scoring
  if (awarenessAnswer >= 3) score += 2;

  const recommendation = score >= 6 ? "complete" : "starter";

  // Generate personalized diagnosis
  let diagnosis = "";
  
  if (lang === "en") {
    if (incomeAnswer <= 2 && situationAnswer <= 2) {
      diagnosis = `Based on your answers, here is what is happening with your money —

Your salary is most likely leaking in small amounts across food delivery, forgotten subscriptions, and unplanned spending. You are not bad with money. You simply do not have a system yet.

The good news: people in your situation typically save ₹2,000–₹5,000 more per month within the first 30 days of using a proper budgeting system. Not by earning more. By stopping the leaks.`;
    } else if (incomeAnswer >= 4 || (goalAnswer >= 4 && awarenessAnswer >= 3)) {
      diagnosis = `Based on your answers, here is what is holding you back —

You earn well, but your money is not working for you yet. EMIs, subscriptions, and untracked spending are taking more than you realise. Your SIP targets are probably not being met consistently.

The good news: people at your income level who put a complete financial system in place typically recover ₹15,000–₹25,000 of invisible leakage per month — and start seeing real wealth building within 90 days.`;
    } else {
      diagnosis = `Based on your answers, here is what we see —

You have good instincts about money, but your current approach is not giving you the visibility you need. Small expenses are adding up without you noticing, and your savings could be significantly higher.

The good news: with the right system in place, people like you typically increase their monthly savings by 30-50% within the first month — without changing their lifestyle.`;
    }
  } else {
    if (incomeAnswer <= 2 && situationAnswer <= 2) {
      diagnosis = `आपके जवाबों के आधार पर, आपके पैसों के साथ ये हो रहा है —

आपकी सैलरी सबसे ज़्यादा छोटी-छोटी चीज़ों में leak हो रही है — food delivery, भूले हुए subscriptions, और unplanned खर्चे। आप पैसों में बुरे नहीं हैं। बस अभी तक कोई system नहीं है।

अच्छी खबर: आपकी situation में लोग typically ₹2,000–₹5,000 ज़्यादा बचाते हैं हर महीने — proper budgeting system use करने के पहले 30 दिनों में। ज़्यादा कमा के नहीं। बस leaks रोक के।`;
    } else if (incomeAnswer >= 4 || (goalAnswer >= 4 && awarenessAnswer >= 3)) {
      diagnosis = `आपके जवाबों के आधार पर, ये है जो आपको रोक रहा है —

आप अच्छा कमाते हैं, पर आपका पैसा आपके लिए काम नहीं कर रहा अभी। EMIs, subscriptions, और untracked खर्चे आपके सोचे से ज़्यादा ले रहे हैं। आपके SIP targets शायद consistently पूरे नहीं हो रहे।

अच्छी खबर: आपकी income level पर जो लोग complete financial system लगाते हैं, वो typically ₹15,000–₹25,000 की invisible leakage recover करते हैं हर महीने — और 90 दिनों में real wealth building देखना शुरू करते हैं।`;
    } else {
      diagnosis = `आपके जवाबों के आधार पर, ये है जो हम देख रहे हैं —

पैसों के बारे में आपकी instincts अच्छी हैं, पर आपका current approach वो visibility नहीं दे रहा जो चाहिए। छोटे खर्चे add up हो रहे हैं बिना आपके notice किए, और आपकी savings significantly ज़्यादा हो सकती है।

अच्छी खबर: सही system के साथ, आप जैसे लोग typically अपनी monthly savings 30-50% बढ़ाते हैं पहले महीने में — बिना lifestyle बदले।`;
    }
  }

  return { diagnosis, recommendation, score };
};
